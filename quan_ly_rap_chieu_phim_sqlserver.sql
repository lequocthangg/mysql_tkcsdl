IF DB_ID(N'QuanLyRapChieuPhimDB') IS NULL
BEGIN
    CREATE DATABASE QuanLyRapChieuPhimDB;
END;
GO

USE QuanLyRapChieuPhimDB;
GO

IF OBJECT_ID(N'dbo.TRG_TICKET_VALIDATE_ROOM', N'TR') IS NOT NULL
    DROP TRIGGER dbo.TRG_TICKET_VALIDATE_ROOM;
GO

DROP TABLE IF EXISTS dbo.PAYMENT;
DROP TABLE IF EXISTS dbo.TICKET;
DROP TABLE IF EXISTS dbo.CUSTOMER;
DROP TABLE IF EXISTS dbo.SHOWTIME;
DROP TABLE IF EXISTS dbo.SEAT;
DROP TABLE IF EXISTS dbo.ROOM;
DROP TABLE IF EXISTS dbo.MOVIE;
GO

CREATE TABLE dbo.MOVIE (
    MaPhim NVARCHAR(10) NOT NULL PRIMARY KEY,
    TenPhim NVARCHAR(150) NOT NULL,
    TheLoai NVARCHAR(100) NOT NULL,
    ThoiLuong INT NOT NULL,
    NgayKhoiChieu DATE NOT NULL,
    MoTa NVARCHAR(500) NULL,
    CONSTRAINT CK_MOVIE_ThoiLuong CHECK (ThoiLuong > 0)
);
GO

CREATE TABLE dbo.ROOM (
    MaPhong NVARCHAR(10) NOT NULL PRIMARY KEY,
    TenPhong NVARCHAR(100) NOT NULL,
    LoaiPhong NVARCHAR(30) NOT NULL,
    SucChua INT NOT NULL,
    CONSTRAINT CK_ROOM_SucChua CHECK (SucChua > 0)
);
GO

CREATE TABLE dbo.SEAT (
    MaGhe NVARCHAR(10) NOT NULL PRIMARY KEY,
    MaPhong NVARCHAR(10) NOT NULL,
    HangGhe NVARCHAR(5) NOT NULL,
    SoGhe INT NOT NULL,
    CONSTRAINT UQ_SEAT_Phong_Hang_So UNIQUE (MaPhong, HangGhe, SoGhe),
    CONSTRAINT CK_SEAT_SoGhe CHECK (SoGhe > 0),
    CONSTRAINT FK_SEAT_ROOM FOREIGN KEY (MaPhong) REFERENCES dbo.ROOM(MaPhong)
);
GO

CREATE TABLE dbo.SHOWTIME (
    MaSuat NVARCHAR(10) NOT NULL PRIMARY KEY,
    MaPhim NVARCHAR(10) NOT NULL,
    MaPhong NVARCHAR(10) NOT NULL,
    ThoiGianBatDau DATETIME2(0) NOT NULL,
    ThoiGianKetThuc DATETIME2(0) NOT NULL,
    CONSTRAINT CK_SHOWTIME_ThoiGian CHECK (ThoiGianKetThuc > ThoiGianBatDau),
    CONSTRAINT FK_SHOWTIME_MOVIE FOREIGN KEY (MaPhim) REFERENCES dbo.MOVIE(MaPhim),
    CONSTRAINT FK_SHOWTIME_ROOM FOREIGN KEY (MaPhong) REFERENCES dbo.ROOM(MaPhong)
);
GO

CREATE TABLE dbo.CUSTOMER (
    MaKH NVARCHAR(10) NOT NULL PRIMARY KEY,
    HoTen NVARCHAR(100) NOT NULL,
    SoDienThoai NVARCHAR(20) NOT NULL,
    Email NVARCHAR(100) NOT NULL
);
GO

CREATE TABLE dbo.TICKET (
    MaVe NVARCHAR(10) NOT NULL PRIMARY KEY,
    MaKH NVARCHAR(10) NOT NULL,
    MaSuat NVARCHAR(10) NOT NULL,
    MaGhe NVARCHAR(10) NOT NULL,
    NgayDat DATETIME2(0) NOT NULL,
    TrangThai NVARCHAR(30) NOT NULL,
    GiaVe DECIMAL(12, 2) NOT NULL,
    CONSTRAINT UQ_TICKET_Suat_Ghe UNIQUE (MaSuat, MaGhe),
    CONSTRAINT CK_TICKET_GiaVe CHECK (GiaVe >= 0),
    CONSTRAINT CK_TICKET_TrangThai CHECK (TrangThai IN (N'DaDat', N'DaThanhToan', N'DaHuy')),
    CONSTRAINT FK_TICKET_CUSTOMER FOREIGN KEY (MaKH) REFERENCES dbo.CUSTOMER(MaKH),
    CONSTRAINT FK_TICKET_SHOWTIME FOREIGN KEY (MaSuat) REFERENCES dbo.SHOWTIME(MaSuat),
    CONSTRAINT FK_TICKET_SEAT FOREIGN KEY (MaGhe) REFERENCES dbo.SEAT(MaGhe)
);
GO

CREATE TABLE dbo.PAYMENT (
    MaThanhToan NVARCHAR(10) NOT NULL PRIMARY KEY,
    MaVe NVARCHAR(10) NOT NULL,
    PhuongThuc NVARCHAR(30) NOT NULL,
    ThoiGianThanhToan DATETIME2(0) NOT NULL,
    CONSTRAINT UQ_PAYMENT_MaVe UNIQUE (MaVe),
    CONSTRAINT CK_PAYMENT_PhuongThuc CHECK (PhuongThuc IN (N'TienMat', N'The', N'ChuyenKhoan', N'ViDienTu')),
    CONSTRAINT FK_PAYMENT_TICKET FOREIGN KEY (MaVe) REFERENCES dbo.TICKET(MaVe)
);
GO

CREATE INDEX IX_SEAT_MaPhong ON dbo.SEAT(MaPhong);
CREATE INDEX IX_SHOWTIME_MaPhim ON dbo.SHOWTIME(MaPhim);
CREATE INDEX IX_SHOWTIME_MaPhong ON dbo.SHOWTIME(MaPhong);
CREATE INDEX IX_TICKET_MaKH ON dbo.TICKET(MaKH);
CREATE INDEX IX_TICKET_MaGhe ON dbo.TICKET(MaGhe);
CREATE INDEX IX_PAYMENT_MaVe ON dbo.PAYMENT(MaVe);
GO

CREATE TRIGGER dbo.TRG_TICKET_VALIDATE_ROOM
ON dbo.TICKET
AFTER INSERT, UPDATE
AS
BEGIN
    SET NOCOUNT ON;

    IF EXISTS (
        SELECT 1
        FROM inserted AS i
        JOIN dbo.SHOWTIME AS st ON st.MaSuat = i.MaSuat
        JOIN dbo.SEAT AS s ON s.MaGhe = i.MaGhe
        WHERE st.MaPhong <> s.MaPhong
    )
    BEGIN
        RAISERROR (N'Ghe khong thuoc phong cua suat chieu.', 16, 1);
        ROLLBACK TRANSACTION;
        RETURN;
    END;
END;
GO

